package com.google;

public class Swap2 
{
public static void main(String[] args) 
{
	int n=10,m=20,temp;
	System.out.println("values befor swaping");
	System.out.println("n="+n);
	System.out.println("m="+m);
	temp=n;
	n=m;
	m=temp;
	System.out.println("values after swaping");
	System.out.println("n="+n);
	System.out.println("m="+m);
}
}
